f_num = input("What your first No. :")
s_num = input("What your second No. :")
sum_num = int(f_num) + int(s_num)
print("The sum of the number",sum_num)

diff_num= int(f_num) - int(s_num)
print("The difference of the number",diff_num)

prod_num = int(f_num) * int(s_num)
print("The product of the number",prod_num)

quot_num = int(f_num) / int(s_num)
print("The quotient of the number",quot_num)