#Block of code that are executed together
#Functions use def keyword
def print_name():
    print("My name is Samara ")
    print("I am from Nairobi ")
    print("I am 18 years old ")
    print("My hobbies include dancing and reading")

def add_numbers(num1,num2):
    sum_num = num1 +num2
    print(sum_num)

def sub_numbers(num1 , num2):
    sub_num = num1 - num2
    print(sub_num)

def pro_numbers(num1,num2):
    pro_num = num1 * num2
    print(pro_num)

#calling or invoking the function
print_name()
